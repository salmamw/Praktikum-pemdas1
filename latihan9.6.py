alas = float(input("masukan alas jajaran genjang: "))
tinggi = float(input("masukan tinggi jajaran genjang: 2"))
sisi_a = float(input("masukan nilai sisi a: "))
sisi_b = float(input("masukan nilai sisi b: "))

#Menghitung luas dan keliling jajaran genjang 
luas = (alas * tinggi) 
keliling = 2 * (sisi_a + sisi_b)

#Menampilkan hasil
print("luas jajaran genjang adalah: ", luas, "cm")
print("keliling jajaran genjang adalah: ", keliling, "cm")